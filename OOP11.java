package BIT01;

public class OOP11 {

	public static void main(String[] args) {
		
		// 대입(=), 복합대입연산 : +=, -=, *=, /=, %=(산술+대입)
		int a=10;
		System.out.println(a+=1); //11
		System.out.println(a-=1); //10
		System.out.println(a*=2); //20
		System.out.println(a/=2); //10
		System.out.println(a%=3); //1
		
	}

}
