package BIT01;

public class OOP14 {

	public static void main(String[] args) {
		// 단행연산자(++, --, !)
		int x=10;
		x++;
		++x;
		System.out.println(x);
		
		int y=10;
		System.out.println(y++); //10
		System.out.println(++y); //12
		
		System.out.println(--y); //11
		System.out.println(y--); //11	
		
	}

}
