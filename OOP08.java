package BIT01;

public class OOP08 {

	public static void main(String[] args) {
		
		int a=10;
		System.out.println(a>10); //false
		System.out.println(a<10); //false
		System.out.println(a>=10); //True
		System.out.println(a<=10); //True
		System.out.println(a==10); //True
		System.out.println(a!=10); //false
		
	}

}
