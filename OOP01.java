package BIT01;

public class OOP01 {

	public static void main(String[] args) {
		
		int a = 10;
		System.out.println(a);
		
		float b = 25.6f; //float는 부동소수 7자까
		System.out.println(b);
		
		
		char c = 'A';
		System.out.println(c);
		
		boolean d = true;
		System.out.println(d);
		
		String e = "홍길동"; //문자
		System.out.println(e);
		
		
	}

}
